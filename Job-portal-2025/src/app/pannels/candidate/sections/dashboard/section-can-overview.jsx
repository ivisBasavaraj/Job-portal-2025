import React, { useState, useEffect } from 'react';
import CountUp from "react-countup";

function SectionCandidateOverview() {
	const [stats, setStats] = useState({
		applied: 0,
		inProgress: 0,
		shortlisted: 0
	});
	const [candidate, setCandidate] = useState({ name: 'Loading...' });

	useEffect(() => {
		fetchDashboardData();
	}, []);

	const fetchDashboardData = async () => {
		try {
			const token = localStorage.getItem('candidateToken');
			if (!token) return;

			const response = await fetch('http://localhost:5000/api/candidate/dashboard/stats', {
				headers: { 'Authorization': `Bearer ${token}` }
			});
			
			if (response.ok) {
				const data = await response.json();
				setStats(data.stats);
				setCandidate(data.candidate);
			}
		} catch (error) {
			console.error('Error fetching dashboard data:', error);
		}
	};

	const cards = [
		{
			bg: "#e0f7fa",
			icon: "flaticon-resume",
			color: "text-info",
			count: stats.applied,
			label: "Applied",
		},
		{
			bg: "#fff3e0",
			icon: "flaticon-envelope",
			color: "text-warning",
			count: stats.inProgress,
			label: "In Progress",
		},
		{
			bg: "#e8f5e9",
			icon: "flaticon-bell",
			color: "text-success",
			count: stats.shortlisted,
			label: "Shortlisted",
		},
	];

	return (
		<>
			<div className="wt-admin-right-page-header">
				<h2>{candidate.name}</h2>
			</div>

			<div className="twm-dash-b-blocks mb-5">
				<div className="row">
					{cards.map((card, index) => (
						<div className="col-xl-4 col-lg-4 col-md-12 mb-3" key={index}>
							<div className="panel panel-default">
								<div
									className="panel-body wt-panel-body dashboard-card-2"
									style={{ backgroundColor: card.bg }}
								>
									<div
										className="d-flex align-items-center"
										style={{ display: "flex", justifyContent: "flex-end" }}
									>
										<div
											className={`wt-card-icon-2 me-3 fs-2 ${card.color}`}
											style={{ lineHeight: "1" }}
										>
											<i className={card.icon} />
										</div>

										<div>
											<div className={`counter fw-bold fs-4 ${card.color}`}>
												<CountUp end={card.count} duration={2} />
											</div>
											<h5 className="mb-0 mt-1">{card.label}</h5>
										</div>
									</div>
								</div>
							</div>
						</div>
					))}
				</div>
			</div>
		</>
	);
}

export default SectionCandidateOverview;
